package cmticheck;

/**
 *
 * @author Dell
 */
public class CodeContent {
    
    private int lineNo;
    private String lineContent;
    private int cncVal;
    private int ctcVal;
    private int ciVal;
    private int twVal;

    /**
     * @return the lineNo
     */
    public int getLineNo() {
        return lineNo;
    }

    /**
     * @param lineNo the lineNo to set
     */
    public void setLineNo(int lineNo) {
        this.lineNo = lineNo;
    }

    /**
     * @return the lineContent
     */
    public String getLineContent() {
        return lineContent;
    }

    /**
     * @param lineContent the lineContent to set
     */
    public void setLineContent(String lineContent) {
        this.lineContent = lineContent;
    }

    /**
     * @return the cncVal
     */
    public int getCncVal() {
        return cncVal;
    }

    /**
     * @param cncVal the cncVal to set
     */
    public void setCncVal(int cncVal) {
        this.cncVal = cncVal;
    }

    /**
     * @return the ctcVal
     */
    public int getCtcVal() {
        return ctcVal;
    }

    /**
     * @param ctcVal the ctcVal to set
     */
    public void setCtcVal(int ctcVal) {
        this.ctcVal = ctcVal;
    }

    /**
     * @return the ciVal
     */
    public int getCiVal() {
        return ciVal;
    }

    /**
     * @param ciVal the ciVal to set
     */
    public void setCiVal(int ciVal) {
        this.ciVal = ciVal;
    }

    /**
     * @return the twVal
     */
    public int getTwVal() {
        return twVal;
    }

    /**
     * @param twVal the twVal to set
     */
    public void setTwVal(int twVal) {
        this.twVal = twVal;
    }
    
}
